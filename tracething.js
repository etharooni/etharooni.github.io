var Traj = {
  N: 0,
  E: 1,
  S: 2,
  W: 3,
  NE: 4,
  NW: 5,
  SW: 6,
  SW: 7,
};

function Trace(initx=0, inity=0, initTraj=Traj.N, initGrid={}){
	this.x = initx;
	this.y = inity;
	this.startx = initx;
	this.starty = inity;
	this.trajectory = initTraj;
	this.grid = initGrid;
	this.length = 0;
	this.vertices = [];
	this.ended = false;
	this.drawnStartVia = false;
	this.drawnEndVia = false;

	this.doTrajectoryIfValid = function(tryTrajectory) {
		var newcoord = this.executeTrajectory(this.x, this.y, tryTrajectory);
		var newx = newcoord[0];
		var newy = newcoord[1];
		if(this.isValid(newx,newy,tryTrajectory)){
			this.drawSegment(this.x,this.y,newx,newy);
			this.x = newx;
			this.y = newy;
			this.trajectory = tryTrajectory;
			this.grid.setPixel(newx, newy, 0);
			this.length += 1;
			this.drawVias();
			return 1;
		}else{
			return 0;
		}
	}
	this.executeTrajectory = function(x, y, trajectory){
		var newx = -1;
		var newy = -1;
		switch (trajectory){
			case Traj.N:
				newy = this.y-1;
				newx = this.x;
				break;
			case Traj.E:
				newy = this.y;
				newx = this.x+1;
				break;
			case Traj.S:
				newy = this.y+1;
				newx = this.x;
				break;
			case Traj.W:
				newy = this.y;
				newx = this.x-1;
				break;
			case Traj.NE:
				newy = this.y-1;
				newx = this.x+1;
				break;
			case Traj.NW:
				newy = this.y-1;
				newx = this.x-1;
				break;
			case Traj.SE:
				newy = this.y+1;
				newx = this.x+1;
				break;
			case Traj.SW:
				newy = this.y+1;
				newx = this.x-1;
				break;
		}
		return [newx,newy]
	}
	this.doNext = function() {
		if(this.ended){
			return -1;
		}
		if(this.length == 0){
			if(this.isValid(this.x, this.y) == 0){
				this.ended = true;
				return 0;
			}
			this.grid.setPixel(this.x, this.y, 0);
		}
		if(this.doTrajectoryIfValid(this.trajectory)){
			return 1; //existing path succeeded
		}else{
			var tryfirst = Math.round(Math.random()); //0 or 1
			var i; //0 to 1
			for (i=0; i<2; i++){
				var nextTrajectory;
				var totry = i*tryfirst;
				switch(this.trajectory){
					case Traj.N:
						if(totry){
							nextTrajectory = Traj.NE;
						}else{
							nextTrajectory = Traj.NW;
						}
						break;
					case Traj.E:
						if(totry){
							nextTrajectory = Traj.NE;
						}else{
							nextTrajectory = Traj.SE;
						}
						break;
					case Traj.S:
						if(totry){
							nextTrajectory = Traj.SE;
						}else{
							nextTrajectory = Traj.SW;
						}
						break;
					case Traj.W:
						if(totry){
							nextTrajectory = Traj.NW;
						}else{
							nextTrajectory = Traj.SW;
						}
						break;
					case Traj.NE:
						if(totry){
							nextTrajectory = Traj.N;
						}else{
							nextTrajectory = Traj.E;
						}
						break;
					case Traj.NW:
						if(totry){
							nextTrajectory = Traj.N;
						}else{
							nextTrajectory = Traj.W;
						}
						break;
					case Traj.SE:
						if(totry){
							nextTrajectory = Traj.S;
						}else{
							nextTrajectory = Traj.E;
						}
						break;
					case Traj.SW:
						if(totry){
							nextTrajectory = Traj.S;
						}else{
							nextTrajectory = Traj.W;
						}
						break;
				}
				if(this.doTrajectoryIfValid(nextTrajectory)){
					this.vertices.push([this.x, this.y, this.length]);
					return 2;
				}
			}
			this.ended = true;
			this.drawVias();
			return 0; //end here.  Nothing left to do.
		}
	}
	this.isValid = function(x,y,trajectory){
		if(this.grid.lutLookup(x,y) == false){
			return false;
		}//could also add 45 degree check, but looks better without
		return true;
	}
	this.drawSegment = function(x1, y1, x2, y2) {
		var cvs1 = this.grid.toCanvas(x1,y1);
		var cvs2 = this.grid.toCanvas(x2,y2);
		ctx.strokeStyle = '#00DCDC';
		ctx.beginPath();
		ctx.moveTo(cvs1[0], cvs1[1]);
		ctx.lineTo(cvs2[0], cvs2[1]);
		ctx.stroke();
	}
	this.drawVias = function(){
		if(this.length < 1){
			return;
		}
		if(!this.drawnStartVia){
			var cvsxy = this.grid.toCanvas(this.startx,this.starty);
			ctx.beginPath();
			ctx.strokeStyle = '#00DCDC';
			ctx.arc(cvsxy[0],cvsxy[1], 2, 0, 2 * Math.PI);
			ctx.stroke();
			this.drawnStartVia = true;
		}
		if(!this.drawnEndVia){
			if(this.ended){
				var cvsxy = this.grid.toCanvas(this.x,this.y);
				ctx.beginPath();
				ctx.strokeStyle = '#00DCDC';
				ctx.arc(cvsxy[0],cvsxy[1], 2, 0, 2 * Math.PI);
				ctx.stroke();
				this.drawnEndVia = true;
			}
		}
	}
};

function Grid(initLut=[], initWidth=0, initHeight=0, x=0, y=0, initCvsWidth=0, initCvsHeight=0){
	this.lut = initLut;
	this.width = initWidth;
	this.height = initHeight;
	this.cvsWidth = initCvsWidth;
	this.cvsHeight = initCvsHeight;
	this.startx = x;
	this.starty = y;
	
	this.toCanvas = function(x,y){
		var position = [x*initCvsWidth/initWidth+this.startx, y*initCvsHeight/initHeight+this.starty];
		return position;
	}
	this.lutLookup = function(x,y){
		if(x >= this.width){
			return false;
		}
		if(y >= this.height){
			return false;
		}
		if(x<0){
			return false;
		}
		if(y<0){
			return false;
		}
		if(this.lut[y][x] == 0){
			return false;
		}
		return true;
	}
	this.setPixel = function(x,y,value=0){
		this.lut[y][x] = value;
	}
	this.drawGrid = function(){
		var x;
		var y;
		for(x=0; x<this.width; x++){
			for(y=0; y<this.height; y++){
				if(this.lutLookup(x,y)){
					var pos = this.toCanvas(x,y);
					ctx.fillStyle = '#FFFFFFFF';
					ctx.fillRect(pos[0],pos[1],1,1);
				}
			}
		}
	}
}

function Visualizer(initGrid ={}){
	this.traces = [];
	this.grid = initGrid;
	this.numEnded = 0;
	
	this.createRandomTrace = function(){
		for(let i=0; i<20; i++){ //todo: optimize.  For now, give up if it takes too long
			var x = Math.floor(Math.random()*grid.width);
			var y = Math.floor(Math.random()*grid.height);
			var traj = Math.floor(Math.random()*7);
			if(this.grid.lutLookup(x,y)){
				var newtrace = new Trace(x, y, traj, grid);
				this.traces.push(newtrace);
				return newtrace;
			}
		}
	}
	this.generateTraces = function(){
		for(let i=0; i<1500;i++){
			var newtrace = this.createRandomTrace();
		}
	}
	this.updateRandomSegment = function(){
		var idx = Math.floor(Math.random()*this.traces.length);
		var endedTrace = this.traces[idx].doNext() == 0;
		if(endedTrace){
			if(this.traces[idx].length < 1){
				this.traces.splice(idx, 1);
			}else{
				this.numEnded++;
			}
		}
	}
}

